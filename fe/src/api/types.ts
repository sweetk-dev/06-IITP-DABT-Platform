// API 관련 공통 타입 정의

// ============================================================================
// API 호출 상태 타입
// ============================================================================

export interface ApiState<T> {
  data: T | null;
  loading: boolean;
  error: string | null;
}

// ============================================================================
// API 훅 반환 타입
// ============================================================================

export interface ApiHookReturn<T> extends ApiState<T> {
  execute: () => Promise<void>;
  refetch: () => Promise<void>;
}
